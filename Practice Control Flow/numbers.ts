'use strict'

function addTwoNumbers(a:number, b:number):number{
    return a+b;
}

function substractTwoNumbers(c:number, d:number):number{
    return c-d;
}

function multiplier(e:number, f:number):number{
    return e*f;
}

function divider(g:number, h:number):number {
    return g/h;
}

function remainder(i:number, j:number):number {
    return i%j;
}

console.log(addTwoNumbers(13,22));
console.log(substractTwoNumbers(22,13));
console.log(multiplier(22,13));
console.log(divider(22,13));
console.log(remainder(22,13));




